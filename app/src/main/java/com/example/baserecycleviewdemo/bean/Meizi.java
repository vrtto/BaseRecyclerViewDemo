public class Meizi {
    private String url;
    private String who;

    public Meizi(String url, String who) {
        this.url = url;
        this.who = who;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getWho() {
        return who;
    }

    public void setWho(String who) {
        this.who = who;
    }
}
